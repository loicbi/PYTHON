def flatteningJSON(b):
    ans = {}

    def flat(i, na=''):
        # nested key-value pair: dict type
        if type(i) is dict:
            for a in i:
                flat(i[a], na + a + '_')
        # nested key-value pair: list type
        elif type(i) is list:
            j = 0
            for a in i:
                flat(a, na + str(j) + '_')
                j += 1
        else:
            ans[na[:-1]] = i

    flat(b)
    return ans


